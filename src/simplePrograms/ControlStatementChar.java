package simplePrograms;

public class ControlStatementChar 
{
	public static void main(String[] args)
	{
		System.out.println("Printing A to Z using for loop");
		for(char i = 'A'; i<='Z'; i++)
		{
			System.out.println(i);
		}
		System.out.println("*********End OF Program********");
			
	}

}
